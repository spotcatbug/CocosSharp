	/// <summary>
	/// Modified from http://stackoverflow.com/questions/10889743/cocos2d-2-0-3-numbers-on-the-bottom-left
	/// by Steffen Itterheim:
	/// 
	/// 5623459 -- memory consumption in bytes
	/// 3       -- garbage collection counter (always 0 when in iOS simulator, see below)
	/// 082     -- number of draw calls
	/// 0.023   -- time it took to update the frame
	/// 0.016   -- time it took to draw the frame
	/// 60.0    -- frames per second
	/// 
	/// The Draw Call number is the number of draw calls (which is fairly high). Typically each node that renders
	/// something on the screen (sprites, labels, particle fx, etc) increases that number by one. If you use
	/// a CCSpriteBatchNode and add 100 sprites to it, it will increase the draw call only by 1.
	/// 
	/// 82 is a pretty high draw call number - depending on the game's complexity and assuming it is well optimized to
	/// reduce draw calls, the number of draw calls should be around 10 to 40. Assuming all 82 draw calls are sprites,
	/// then creating a texture atlas out of the sprite images (use TexturePacker, Zwoptex, SpriteHelper) in order to
	/// use CCSpriteBatchNode you could reduce the number of draw calls to 1. Draw calls are expensive, so it is very
	/// important to keep that number down.
	/// 
	/// The time it took to render a frame is in milliseconds. Since you need to draw a new frame every 0.016666666
	/// seconds in order to achieve 60 frames per second (1/60 = 0,0166...) this number can tell you how close your game
	/// is to dropping below 60 fps. Yours is pretty close, you have practically no room left for additional game logic
	/// or visuals before the framerate will drop below 60 fps.
	/// 
	/// The last number is the number of frames per second. This value, like the previous one, is averaged over several
	/// frames so that it doesn't fluctuate as much (makes it hard to read).
	/// 
	/// PS: one other thing to keep in mind is that the bottom two values become misleading can not be compared for
	/// framerates below 15 fps. For example cocos2d might show 0.0 for the time it took to render a frame at such
	/// a low framerate.
	/// 
	/// There is a special case for Xamarin iOS monotouch on emulator where they aggresively call 
	/// garbage collection themselves on the simulator. This should not affect the devices though.
	/// So we check if we are running on a Device and only update the counters if we are.
	/// </summary>
	public class CCStats
	{


		bool isGCEnabled;
		bool isInitialized, isEnabled;
		uint totalFrames = 0;
		uint updateCount;
		uint totalDrawCount;
		int gcCounter;
		float deltaAll = 0.0f;
		float totalDrawTime;
		float totalUpdateTime;
		float startTime;
		int scale;

		Stopwatch stopwatch;

		WeakReference gcWeakRef = new WeakReference (new object ());

		CCLabelAtlas fpsLabel;
		CCLabelAtlas updateTimeLabel;
		CCLabelAtlas drawTimeLabel;
		CCLabelAtlas drawCallLabel;
		CCLabelAtlas memoryLabel;
		CCLabelAtlas gcLabel;

		CCColor3B warnColor = new CCColor3B (255, 151, 151);
		CCColor3B whiteColor = CCColor3B.White;


		#region Constructor


		#endregion


		#region Public


		/// <summary>
		/// Initialize CCStats.
		/// </summary>
		public void Initialize ()
		{
			if (!isInitialized) {
				// There is a special case for Xamarin iOS monotouch on emulator where they aggresively call 
				// garbage collection themselves on the simulator. This should not affect the devices though.
				// So we check if we are running on a Device and only update the counters if we are.
				#if IOS
				isGCEnabled = (Runtime.Arch == Arch.DEVICE);
				#else
				isGCEnabled = true;
				#endif

				CCTexture2D texture;
				CCTextureCache textureCache = CCTextureCache.SharedTextureCache;

				stopwatch = new Stopwatch ();

				try {
					var surfaceFormat = CCSurfaceFormat.Color;

					#if WINDOWS_PHONE81
					surfaceFormat = CCSurfaceFormat.Color;
					#endif

					texture = !textureCache.Contains ("cc_fps_images") ? textureCache.AddImage (CCFPSImage.PngData, "cc_fps_images", surfaceFormat) : textureCache ["cc_fps_images"];

					// analysis disable CompareOfFloatsByEqualityOperator
					if (texture == null || (texture.ContentSizeInPixels.Width == 0 && texture.ContentSizeInPixels.Height == 0)) {
						CCLog.Log ("CCStats2: Failed to create stats texture");
						return;
					}
				} catch (Exception ex) {
					// MonoGame may not allow texture.fromstream,
					// so catch this exception here and disable the stats
					if (ex != null)
						CCLog.Log ("CCStats2: Failed to create stats texture: {0}", ex);
					else
						CCLog.Log ("CCStats2: Failed to create stats texture");
					return;
				}

				// we will remove the texture cc_fps_images from our cache to fix a problem of loosing the texture
				// when the cache is purged.  If not then the statistics no longer show because it has been Disposed of.
				textureCache.RemoveTextureForKey ("cc_fps_images");

				try {
					texture.IsAntialiased = false; // disable antialiasing so the labels are always sharp

					fpsLabel = new CCLabelAtlas ("00.0", texture, 8, 15, ' ');
					updateTimeLabel = new CCLabelAtlas ("0.000", texture, 8, 15, ' ');
					drawTimeLabel = new CCLabelAtlas ("0.000", texture, 8, 15, ' ');
					drawCallLabel = new CCLabelAtlas ("000", texture, 8, 15, ' ');
					memoryLabel = new CCLabelAtlas ("0", texture, 8, 15, ' ');
					gcLabel = new CCLabelAtlas ("0", texture, 8, 15, ' ');

					memoryLabel.Color = new CCColor3B (255, 196, 54);
					drawCallLabel.Color = new CCColor3B (152, 217, 255);
				} catch (Exception ex) {
					if (ex != null)
						CCLog.Log ("CCStats: Failed to create stats labels: {0}", ex);
					else
						CCLog.Log ("CCStats: Failed to create stats labels");
					return;
				}

				isInitialized = true;
				Scale = 1;
			}
		}


		public void UpdateStart ()
		{
			if (isEnabled) {
				startTime = (float)stopwatch.Elapsed.TotalMilliseconds;
			}
		}


		public void UpdateEnd (float delta)
		{
			if (isEnabled) {
				deltaAll += delta;
				updateCount++;
				totalUpdateTime += (float)stopwatch.Elapsed.TotalMilliseconds - startTime;
			}
		}


		public void Draw (CCWindow window)
		{
			if (isEnabled) {
				totalFrames++;
				totalDrawCount++;
				totalDrawTime += (float)stopwatch.Elapsed.TotalMilliseconds - startTime;

				if (isGCEnabled && !gcWeakRef.IsAlive) {
					gcCounter++;
					gcWeakRef = new WeakReference (new object ());
				}

				if (isInitialized) {
					if (deltaAll > 0.5f) {
						var mem = GC.GetTotalMemory (false);
						var upd = totalUpdateTime / updateCount;
						var drw = totalDrawTime / totalDrawCount;
						var fps = totalDrawCount / deltaAll;

						fpsLabel.Text =        String.Format ("             {0:0} FPS", fps);
						updateTimeLabel.Text = String.Format ("Update time: {0:0.000} ms", upd);
						drawTimeLabel.Text =   String.Format ("Draw time:   {0:0.000} ms", drw);
						drawCallLabel.Text =   String.Format ("Drawcalls:   {0}", window.DrawManager.DrawCount);
						memoryLabel.Text =     String.Format ("Memory:      {0} B ({1} kB)", mem, mem / 1024);
						gcLabel.Text =         String.Format ("Garbage:     #{0}", gcCounter);

						deltaAll = totalDrawTime = totalUpdateTime = 0;
						totalDrawCount = updateCount = 0;

						drawTimeLabel.Color = drw > 16.666f ? warnColor : whiteColor;
						updateTimeLabel.Color = upd > 16.666f ? warnColor : whiteColor;
						fpsLabel.Color = fps < 30 ? warnColor : whiteColor;
					}

					var scene = window.DefaultDirector.RunningScene;
					drawCallLabel.Scene = scene;
					fpsLabel.Scene = scene;
					updateTimeLabel.Scene = scene;
					drawTimeLabel.Scene = scene;
					memoryLabel.Scene = scene;
					gcLabel.Scene = scene;

					drawCallLabel.Visit ();
					fpsLabel.Visit ();
					updateTimeLabel.Visit ();
					drawTimeLabel.Visit ();
					memoryLabel.Visit ();
					gcLabel.Visit ();
				}
			}
		}


		public int Scale {
			get { return scale; }
			set {
				var pos = CCPoint.Zero;

				scale = value;

				if (isInitialized) {
					var i = 0;
					const int step = 14;

					fpsLabel.Scale = scale;
					updateTimeLabel.Scale = scale;
					drawTimeLabel.Scale = scale;
					drawCallLabel.Scale = scale;
					memoryLabel.Scale = scale;
					gcLabel.Scale = scale;

					fpsLabel.Position = new CCPoint (4 * scale, i++ * step * scale + 4) + pos;
					drawTimeLabel.Position = new CCPoint (4 * scale, i++ * step * scale + 4) + pos;
					updateTimeLabel.Position = new CCPoint (4 * scale, i++ * step * scale + 4) + pos;
					gcLabel.Position = new CCPoint (4 * scale, i++ * step * scale + 4) + pos;
					drawCallLabel.Position = new CCPoint (4 * scale, i++ * step * scale + 4) + pos;
					memoryLabel.Position = new CCPoint (4 * scale, i++ * step * scale + 4) + pos;
				}
			}
		}


		public bool IsInitialized {
			get { return isInitialized; }
		}


		public bool IsEnabled {
			get { return isEnabled; }
			set {
				isEnabled = value;

				if (value) {
					stopwatch.Reset ();
					stopwatch.Start ();
				}
			}
		}


		#endregion Properties


	}


